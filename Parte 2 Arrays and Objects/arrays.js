const names = [
    "Luis", // Posicion 0
    "David", // Posicion 1
    "Correa" // Posicion 2
]
console.log(names.length, "Datos tiene este arreglo");
// Cuantos datos tiene este arreglo
console.log(names[0]);
console.log(names[1]);
console.log(names[2]);
// los arreglos son principalmente para almacenar uno  o mas valores dentro de una variable y reducen la creacion de variables para diferentes datos
